# Basic Python Script

# Ask for user input
name = input("What is your name? ")
age = int(input("How old are you? "))

# Calculate the year the user will turn 100
current_year = 2025
year_turn_100 = current_year + (100 - age)

# Print the result
print(f"Hello, {name}!")
print(f"You will turn 100 years old in the year {year_turn_100}.")
